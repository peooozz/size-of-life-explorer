export const getScaleForViewport = (organismSize: number, viewportHeight: number): number => {
  // Target: organism takes up 60% of viewport height
  const targetPixels = viewportHeight * 0.6;
  // Convert mm to pixels (assuming 96 DPI, 1 inch = 25.4mm)
  const pixelsPerMm = 96 / 25.4;
  const organismPixels = organismSize * pixelsPerMm;
  
  return targetPixels / organismPixels;
};
